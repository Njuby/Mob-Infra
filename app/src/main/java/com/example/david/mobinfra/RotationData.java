package com.example.david.mobinfra;

public class RotationData {

    public float x;
    public float y;
    public float z;


    public RotationData(float x, float y, float z){
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
